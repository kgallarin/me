import { writeFileSync } from 'fs';
import { dirname, resolve } from 'path';
import { fileURLToPath } from 'url';

import { generate } from 'critical';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');

const BASE_URL = process.env.APP_URL ?? 'http://localhost:8000';

console.log(`Generating critical CSS from ${BASE_URL} ...`);

let css;

try {
	({ css } = await generate({
		// Page to render
		src: BASE_URL,
		base: resolve(projectRoot, 'public'),

		inline: false,
		dimensions: [
			{ width: 375, height: 812 },
			{ width: 768, height: 1024 },
			{ width: 1280, height: 800 },
			{ width: 1440, height: 900 },
		],

		penthouse: {
			renderWaitTime: 300,
			keepLargerMediaQueries: false,
		},
	}));
} catch (err) {
	console.error('\nFailed to generate critical CSS.');
	console.error('Make sure your Laravel server is running at:', BASE_URL);
	console.error('  php artisan serve\n');
	console.error(err.message);
	process.exit(1);
}

const outputPath = resolve(projectRoot, 'resources/views/components/critical-css.blade.php');

const bladeContent = `{{--
    Auto-generated by scripts/generate-critical.mjs
    Source: ${BASE_URL}
    Generated: ${new Date().toISOString()}

    DO NOT edit manually — run \`npm run build:critical\` to regenerate.
    Commit this file after regenerating so production uses the latest version.
--}}
<style>
${css}
</style>
`;

writeFileSync(outputPath, bladeContent);

console.log('Critical CSS written to:');
console.log(' ', outputPath);
console.log(`\nSize: ${(css.length / 1024).toFixed(1)} KB`);
